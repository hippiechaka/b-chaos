				<div class="col-md-10">
					<p><strong>B Chaos</strong> is a Groove Metal band formed in Guadalajara México in the 2012 year.</p> 
					<p>Diego García (Gambo) Voice; David Martínez (Brujo) on Bass; Fernando Luna (Barbas) & Armando García (Butch) on Guitars and Roy Ochoa on Drums are responsible for the creation of this sound machine. In 2013 they recorded their first single ¨Groove Shot¨ at Big Thunder Studios in Guadalajara Jalisco México and which was mastered at Fascination Street Studio in Österáng Örebro Sweden.</p> 
					<p>In 2015 they released their first EP <strong>¨Omen¨</strong> with three new tracks recorded at Boogie Studios in Guadalajara México. Co-produced by B Chaos & Galileo Ochoa & Mastered at PLX Mastering in New York by Paul Logus (Pantera, Paul Gilbert, NIN, Opeth, Trivium, etc.)</p>
					<p>By the end of 2016, together with Dog Union, they visited several cities in México during their "Spread the Chaos" tour showing their great quality in live performance. Everybody who attended the shows  were greatly pleased.</p>
					<p><strong>Ashes</strong>, their second independent production (2018). Eleven new tracks make up this powerful album. Co-produced by B Chaos & Galileo Ochoa at Boogie Studios, mixed by Ricardo Partida & mastered by Alejandro Lepe at Sonocomio Studios in Guadalajara, México.</p> 
					<p>They have been in shows all over México during the 6 years they have existed. They have performed in festivals like Rock n Roll marathon CDMX (2016), Moto Teotihuacán (2017), Cocupaluza (2018),  sharing the stage with bands such as: <strong>Courney Cox &amp; Nicky Stringfield</strong> (The Iron Maidens), <strong>Sin Quirin</strong> (Ministry), <strong>Meegs Rascon</strong> (Coal Chamber), <strong>Brad Jurjens</strong>, <strong>Molotov</strong>, <strong>Nata</strong>, <strong>Thell Barrio</strong>, <strong>QBO</strong>, <strong>Dave Olea</strong>, <strong>Parazit</strong>, <strong>HCTK</strong>, <strong>Pressive</strong>, <strong>Garigoles</strong>, <strong>Salvador y los Eones</strong> , <strong>Greñas</strong> (Luzbel), <strong>Hugo Loyo</strong>, <strong>Tulkas</strong>, <strong>Strike Master</strong>, among others... B Chaos name comes from the mix of 3 band members’ nickname initials; and the Chaos Theory which states that nothing is written; any small change in your life can take you wherever you want.</p>
